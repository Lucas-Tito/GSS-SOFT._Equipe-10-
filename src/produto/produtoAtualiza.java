    public void preencherCampos(produto c){
        String codigo = Integer.toString(c.getCodigo()); /*converte valores do getCodigo para String*/
        campoCodigo.setText(codigo);
       
        campoNome.setText(c.getNome());
        
        String valor = Double.toString(c.getValor());
        campoValor.setText(valor);
       
        String categoria = Integer.toString(c.getCategoria());
        campoCategoria.setText(categoria);
        
        String peso = Double.toString(c.getPeso());
        campoPeso.setText(peso);
                
    }
