    public void preencherCampos() {
       gerenteDAO dao = new gerenteDAO();
        ArrayList<funcio> funcios = dao.listar();
        /*Precisa armazenar o id dentro do parentese de dao.consultar (criar objeto com o id pra delcarar no parentese)*/
       tabC = (DefaultTableModel) tabConsulta.getModel();

        while (tabC.getRowCount() > 0) {
            tabC.removeRow(0);
        }
        int i = 0;
        for (funcio f : funcios) {
            tabC.addRow(new String[i]);
            tabC.setValueAt(f.getId(), i, 0);
            tabC.setValueAt(f.getNome(), i, 1);
            tabC.setValueAt(f.getCriado(), i, 2);
            tabC.setValueAt(f.getSituacao(), i, 3);
            i++; 
        }
    
    
       
       
       
       
       
       
       
       
       
       
    }
