    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        produto c = new produto();

        int codigo = Integer.parseInt(campoCodigo.getText());
        c.setCodigo(codigo);

        c.setNome(campoNome.getText());

        double valor =  Double.parseDouble(campoValor.getText());    /*converte valores do campoValor para double*/
        c.setValor(valor);

        int categoria = Integer.parseInt(campoCategoria.getText());
        c.setCategoria(categoria);

        double peso =  Double.parseDouble(campoPeso.getText());
        c.setPeso(peso);

        produtoDAO dao = new produtoDAO();
        status.setText(dao.atualizar(c));
        lista.atualizaTabela();

        campoCodigo.setText("");
        campoNome.setText("");
        campoValor.setText("");
        campoCategoria.setText("");
        campoPeso.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

